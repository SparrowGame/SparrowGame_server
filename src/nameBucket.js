let use = {};
let ptr = 0;

function getUnused() {
  let count = 0;
  while (use[nameBucket[ptr]] && count < nameBucket.length) {
    count++;
    ptr = (ptr + 1) % nameBucket.length;
  }
  if (count < nameBucket.length) {
    use[nameBucket[ptr]] = true;
    return nameBucket[ptr];
  }
  return '';
}

function unuse(name){
  delete use[name];
}

